    public void validateFields() {
        String FoodItemName = txtFoodItemName.getText();
        String FoodItemPrice = txtCategory.getText();
        String FoodItemDescription = txtFoodItemDescription.getText();
        if (!FoodItemName.equals("") && (!FoodItemPrice.equals("")) && (!FoodItemDescription.equals(""))) {
            btnCreate.setEnabled(true);
        } else {
            btnCreate.setEnabled(false);
        }
        if (!FoodItemName.equals("") && (!FoodItemPrice.equals("")) && (!FoodItemDescription.equals(""))) {
            btnModify.setEnabled(true);
        } else {
            btnModify.setEnabled(false);
        }
        if (!FoodItemName.equals("") && (!FoodItemPrice.equals("")) && (!FoodItemDescription.equals(""))) {
            btnClear.setEnabled(true);
        } else {
            btnClear.setEnabled(false);
        }
        if (!FoodItemName.equals("") && (!FoodItemPrice.equals("")) && (!FoodItemDescription.equals(""))) {
            btnDelete.setEnabled(true);
        } else {
            btnDelete.setEnabled(false);
        }
    }

