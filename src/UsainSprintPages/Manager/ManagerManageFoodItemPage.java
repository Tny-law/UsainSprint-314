    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

        Iterator<FoodItem> itr2 = ViewMenuItemController.getFoodData();
        while (itr2.hasNext()) {
            FoodItem foodItemObj = itr2.next();
            dtm.addRow(new Object[]{foodItemObj.getFoodItemId(), foodItemObj.getFoodItemName(), foodItemObj.getFoodItemCategory(), foodItemObj.getFoodItemPrice(), foodItemObj.getFoodItemDescription(), foodItemObj.getFoodItemAllergens()});
        }
    }//GEN-LAST:event_formComponentShown

