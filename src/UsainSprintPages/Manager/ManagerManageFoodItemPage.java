    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String FoodItemId = model.getValueAt(index, 0).toString();
        lblFoodItemId.setText(FoodItemId);
        String FoodItemName = model.getValueAt(index, 1).toString();
        txtFoodItemName.setText(FoodItemName);
        String FoodItemCategory = model.getValueAt(index, 2).toString();
        txtCategory.setText(FoodItemCategory);
        String FoodItemPrice = model.getValueAt(index, 3).toString();
        txtFoodItemPrice.setText(FoodItemPrice);
        String FoodItemDescription = model.getValueAt(index, 4).toString();
        txtFoodItemDescription.setText(FoodItemDescription);
        String FoodItemAllergens = model.getValueAt(index, 5).toString();
        txtFoodItemAllergens.setText(FoodItemAllergens);
        btnModify.setEnabled(true);
        btnDelete.setEnabled(true);
        btnClear.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

